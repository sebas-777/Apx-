exports.suma = function (number1, number2) {
    return number1 + number2;
}

exports.restar = function (number1, number2) {
    return number1 - number2;
}

exports.multiplicar = function (number1, number2) {
    return number1 * number2;
}

exports.dividir = function (number1, number2) {
    return number1 / number2;
}